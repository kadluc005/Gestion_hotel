      public void tableFact(){
        String []facture={"Maticule","RÃ©servation","Client", "Total"}; 
        String []afficher=new String[5];

        DefaultTableModel model=new DefaultTableModel(null, facture);
        try{
            Connection conn= DbConnection.getConnection();
            Statement stmt=conn.createStatement();
            ResultSet rs=stmt.executeQuery("SELECT* FROM factures;");
            while(rs.next()){
                afficher[0]=rs.getString("id_facture"); 	 	 	
                afficher[1]=rs.getString("id_reservation");
                afficher[2]=rs.getString("nom_client");
                afficher[3]=rs.getString("total");

                model.addRow(afficher);
                table_fact.setModel(model);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Erreur "+e.getMessage());
        }
    } 
    
